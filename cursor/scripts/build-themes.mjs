/**
 * Builds sayarin-dark.json / sayarin-light.json from palette.oklch.json (OKLCH source → hex for VS Code).
 */
import { readFileSync, writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { formatHex8, parse } from "culori";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const palette = JSON.parse(readFileSync(join(root, "palette.oklch.json"), "utf8"));

function toHex(oklchString) {
  const c = parse(oklchString.trim());
  if (!c) throw new Error(`Invalid color: ${oklchString}`);
  return formatHex8(c);
}

function pick(variant, path) {
  const parts = path.split(".");
  let o = palette[variant];
  for (const p of parts) {
    o = o[p];
    if (o === undefined) throw new Error(`Missing palette ${variant}.${path}`);
  }
  return toHex(o);
}

/** @param {Record<string, string>} map */
function buildColors(variant, map) {
  const out = {};
  for (const [vscodeKey, path] of Object.entries(map)) {
    out[vscodeKey] = pick(variant, path);
  }
  return out;
}

