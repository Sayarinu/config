function buildTokenColors(variant) {
  return tokenRules.map(([scopeStr, syntaxKey, fontStyle]) => {
    const scope = scopeStr.split(",").map((s) => s.trim());
    const fg = pick(variant, `syntax.${syntaxKey}`);
    const settings = { foreground: fg };
    if (fontStyle === "italic") settings.fontStyle = "italic";
    if (fontStyle === "bold") settings.fontStyle = "bold";
    return { scope, settings };
  });
}

